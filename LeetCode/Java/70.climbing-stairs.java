// @lc code=start
class Solution {
    public int climbStairs(int n) {
        int result = 0;
        int dp[] = new int[n+1];
        dp[0] = 1;
        dp[1] = 1;
        for (int i = 2; i <= n; i++) {
            dp[i] = dp[n-1]+dp[n-2];
            result = dp[i];
        }
        return result;
    }
}
